    private void bt_SalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_SalvarActionPerformed
     if(campo_nome_User.getText().isEmpty()){
     JOptionPane.showMessageDialog(null, "Preencha o campo de usuario");
     }else if(campo_senha_User.getText().isEmpty()){
     JOptionPane.showMessageDialog(null, "Preencha o campo de senha");
     }else{
        
        try {
           CadastrarUsuario();
            campo_nome_User.setText("");
        campo_Nome_Func.setText("");
        campo_senha_User.setText("");
        campo_Fk_Codfunc.setText("");
     
            JOptionPane.showMessageDialog(null, "Cadastro realizado do usuário"); // se funcionar
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Cadastro falhou: erro "+e); // se der erro
        }
     }
    }//GEN-LAST:event_bt_SalvarActionPerformed

