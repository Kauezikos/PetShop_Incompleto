    public void CadastrarUsuario(){
    BeanUsuario beanuse = new BeanUsuario(); // cria objeto para acessar as variaveis
    
    //seta nas variaveis os valores dos campos
    
   beanuse.setNome_User(campo_nome_User.getText());
   beanuse.setSenha_User(campo_senha_User.getText());
   beanuse.setFk_cod_Func(Integer.parseInt(campo_Fk_Codfunc.getText()));
        
    DaoUsuario daouse = new DaoUsuario(); // cria objeto para acessar os metodos sql
    
    daouse.inserirDados(beanuse); // coloca o objeto bean para automaticamente ele usar os valores que foram setados 
    
    }
